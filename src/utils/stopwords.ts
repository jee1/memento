/**
 * 한국어와 영어 불용어를 통합한 Set을 반환
 */
export function getStopWords(): Set<string> {
  const allStopWords = [...englishStopWords, ...koreanStopWords];
  return new Set(allStopWords.map(word => word.toLowerCase()));
}

/**
 * 특정 언어의 불용어만 반환
 */
export function getEnglishStopWords(): Set<string> {
  return new Set(englishStopWords.map(word => word.toLowerCase()));
}

export function getKoreanStopWords(): Set<string> {
  return new Set(koreanStopWords.map(word => word.toLowerCase()));
}

/**
 * 불용어인지 확인
 */
export function isStopWord(word: string): boolean {
  const stopWords = getStopWords();
  return stopWords.has(word.toLowerCase());
}
